# Одноклеточная амеба каждые три часа делится на 2 клетки. Определить, сколько будет
# клеток через 6 часов

if __name__ == '__main__':
    n = int(input("Введите количество амеб "))
    n = n * 4
    print("Через такое время амеб будет - ", n)
